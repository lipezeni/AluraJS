// == (comparação impricita)

const numero = 5;
const texto = "5";

console.log(numero == texto);

// === (comparação explicita)

console.log(numero === texto);

// typeof
console.log(typeof numero);
console.log(typeof texto);

// == compara o valor de uma variavel
// === compara o valor e o tipo de uma variavel

// conversao de forma explicita

Number();
String();